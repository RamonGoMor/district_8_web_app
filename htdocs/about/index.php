<td><p><h3>The Inland Empire Transportation Management Center is designed and constructed to house both Caltrans and CHP personnel and provide intermodal, interregional, interagency traffic management services 24/7 for the Inland Empire region and is the command and control emergency center during major catastrophes.</br>The building is a two-story base isolated structure with a total floor space of approximately 45,000 sf and is designed to achieve LEED Gold rating. A 120 foot communications tower is constructed on the site. The IETMC site occupies approximately 10 acres within an approximate 35-acre State-owned parcel located in the southeast quadrant of the interchange of Interstate 15 (I-15) and State Route 210 (SR-210) in the City of Fontana</br></br></br>
 <strong>The IETMC's jurisdictional bounderies include the counties of San Bernardino and Riverside. The Inland Division Dispatch for CHP is co-located in this facility with the building constructed to meet the Essential Services Act criteria. </br>(Caltrans District 8)</strong></br></br>

The IETMC in partnership with the CHP, Caltrans manages the State Highway and Freeway System in District 8 via the TMC. The TMC provides radio communication between TMT field units and office staff. Provides drivers information during special events and major traffic incidents (accidents, fires, snow, etc.) via Changeable Message Signs (CMS) and Highway Advisory Radio (HAR)</h3></p></td>
</tr>

